// Create a function named calculateArea that takes the radius of a circle as a parameter and returns the area of the circle. Use the formula: area = π * radius^2.
// Sample Input and Output:
// •	calculateArea(5)
// Output: 78.53981633974483
// •	calculateArea(10)
// Output: 314.1592653589793

